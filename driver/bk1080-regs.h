#ifndef BK1080_REGS_H
#define BK1080_REGS_H

enum BK1080_Register_t {
	BK1080_REG_00                       = 0x00U,	// No use, default value 0x0006
/*	BK1080_REG_01                       = 0x01U,	// No use, default value 0x1080 */
	BK1080_REG_02_POWER_CONFIGURATION   = 0x02U,
	BK1080_REG_03_CHANNEL               = 0x03U,
	BK1080_REG_04_SYSTEM_CONFIGURATION1 = 0x04U,
	BK1080_REG_05_SYSTEM_CONFIGURATION2 = 0x05U,
	BK1080_REG_06_SYSTEM_CONFIGURATION3 = 0x06U,
	BK1080_REG_07                       = 0x07U,
/*	BK1080_REG_08                       = 0x08U,	// Test 2, default value 0x0000 */
/*	BK1080_REG_09                       = 0x09U,	// Boot Configuration, default value 0x0000 */
	BK1080_REG_10                       = 0x0AU,
	BK1080_REG_11                       = 0x0BU,
	BK1080_REG_25_INTERNAL              = 0x19U,
};

typedef enum BK1080_Register_t BK1080_Register_t;

// REG 02

#define BK1080_REG_02_SHIFT_DSMUTE		15
#define BK1080_REG_02_SHIFT_MUTE		14
#define BK1080_REG_02_SHIFT_MONO		13
#define BK1080_REG_02_SHIFT_SKMODE		10
#define BK1080_REG_02_SHIFT_SEEKUP		9
#define BK1080_REG_02_SHIFT_SEEK                8

#define BK1080_REG_02_MASK_DSMUTE		(0x01U << BK1080_REG_02_SHIFT_DSMUTE)
#define BK1080_REG_02_MASK_MUTE			(0x01U << BK1080_REG_02_SHIFT_MUTE)
#define BK1080_REG_02_MASK_MONO			(0x01U << BK1080_REG_02_SHIFT_MONO)
#define BK1080_REG_02_MASK_SKMODE		(0x01U << BK1080_REG_02_SHIFT_SKMODE)
#define BK1080_REG_02_MASK_SEEKUP		(0x01U << BK1080_REG_02_SHIFT_SEEKUP)
#define BK1080_REG_02_MASK_SEEK			(0x01U << BK1080_REG_02_SHIFT_SEEK)

#define BK1080_REG_02_DSMUTE_ENABLE		(0U << BK1080_REG_02_SHIFT_DSMUTE)
#define BK1080_REG_O2_DSMUTE_DISABLE		(1U << BK1080_REG_02_SHIFT_DSMUTE)

#define BK1080_REG_02_MUTE_OFF			(0U << BK1080_REG_02_SHIFT_MUTE)
#define BK1080_REG_O2_MUTE_ON			(1U << BK1080_REG_02_SHIFT_MUTE)

#define BK1080_REG_02_MONO_STEREO		(0U << BK1080_REG_02_SHIFT_MONO)
#define BK1080_REG_O2_MONO_FORCE		(1U << BK1080_REG_02_SHIFT_MONO)

#define BK1080_REG_02_SKMODE_WRAP		(0U << BK1080_REG_02_SHIFT_SKMODE)
#define BK1080_REG_O2_SKMODE_STOP		(1U << BK1080_REG_02_SHIFT_SKMODE)

#define BK1080_REG_02_SEEK_DOWN			(0U << BK1080_REG_02_SHIFT_SEEKUP)
#define BK1080_REG_O2_SEEK_UP			(1U << BK1080_REG_02_SHIFT_SEEKUP)

#define BK1080_REG_02_SEEK_DISABLE		(0U << BK1080_REG_02_SHIFT_SEEK)
#define BK1080_REG_O2_SEEK_ENABLE		(1U << BK1080_REG_02_SHIFT_SEEK)

#define BK1080_REG_02_GET_DSMUTE(x)		(((x) & BK1080_REG_02_MASK_DSMUTE) >> BK1080_REG_02_SHIFT_DSMUTE)
#define BK1080_REG_02_GET_MUTE(x)		(((x) & BK1080_REG_02_MASK_MUTE) >> BK1080_REG_02_SHIFT_MUTE)
#define BK1080_REG_02_GET_MONO(x)		(((x) & BK1080_REG_02_MASK_MONO) >> BK1080_REG_02_SHIFT_MONO)
#define BK1080_REG_02_GET_SKMODE(x)		(((x) & BK1080_REG_02_MASK_SKMODE) >> BK1080_REG_02_SHIFT_SKMODE)
#define BK1080_REG_02_GET_SEEKUP(x)		(((x) & BK1080_REG_02_MASK_SEEKUP) >> BK1080_REG_02_SHIFT_SEEKUP)
#define BK1080_REG_02_GET_SEEK(x)		(((x) & BK1080_REG_02_MASK_) >> BK1080_REG_02_SHIFT_)

// REG 03

#define BK1080_REG_03_SHIFT_TUNE		15
#define BK1080_REG_03_SHIFT_CHAN		0

#define BK1080_REG_03_MASK_TUNE			(0x01U << BK1080_REG_03_SHIFT_TUNE)
#define BK1080_REG_03_MASK_CHAN			(0xFFU << BK1080_REG_03_SHIFT_CHAN)

#define BK1080_REG_03_TUNE_DISABLE		(0U << BK1080_REG_03_SHIFT_TUNE)
#define BK1080_REG_03_TUNE_ENABLE		(1U << BK1080_REG_03_SHIFT_TUNE)

#define BK1080_REG_03_GET_TUNE(x)		(((x) & BK1080_REG_03_MASK_TUNE) >> BK1080_REG_03_SHIFT_TUNE)
#define BK1080_REG_03_GET_CHAN(x)		(((x) & BK1080_REG_03_MASK_CHAN) >> BK1080_REG_03_SHIFT_CHAN)

// REG 05

#define BK1080_REG_05_SHIFT_SEEKTH              8
#define BK1080_REG_05_SHIFT_BAND                6
#define BK1080_REG_05_SHIFT_SPACE               4
#define BK1080_REG_05_SHIFT_VOLUME              0

#define BK1080_REG_05_MASK_SEEKTH               (0x0FFU << BK1080_REG_05_SHIFT_SEEKTH)
#define BK1080_REG_05_MASK_BAND                 (0x003U << BK1080_REG_05_SHIFT_BAND)
#define BK1080_REG_05_MASK_SPACE                (0x003U << BK1080_REG_05_SHIFT_SPACE)
#define BK1080_REG_05_MASK_VOLUME               (0x00FU << BK1080_REG_05_SHIFT_VOLUME)

#define BK1080_REG_05_GET_SEEKTH                (((x) & BK1080_REG_05_MASK_SEEKTH) >> BK1080_REG_05_SHIFT_SEEKTH)
#define BK1080_REG_05_GET_BAND                  (((x) & BK1080_REG_05_MASK_BAND) >> BK1080_REG_05_SHIFT_BAND)
#define BK1080_REG_05_GET_SPACE                 (((x) & BK1080_REG_05_MASK_SPACE) >> BK1080_REG_05_SHIFT_SPACE)
#define BK1080_REG_05_GET_VOLUME                (((x) & BK1080_REG_05_MASK_VOLUME) >> BK1080_REG_05_SHIFT_VOLUME)

// REG 06

#define BK1080_REG_06_SHIFT_SKMUTER		14
#define BK1080_REG_06_SHIFT_SKMUTEA		12
#define BK1080_REG_06_SHIFT_SKSNR		4
#define BK1080_REG_06_SHIFT_SKCNT		0

#define BK1080_REG_06_MASK_SKMUTER		(0x03U << BK1080_REG_06_SHIFT_SKMUTER)
#define BK1080_REG_06_MASK_SKMUTEA		(0x03U << BK1080_REG_06_SHIFT_SKMUTEA)
#define BK1080_REG_06_MASK_SKSNR		(0x0FU << BK1080_REG_06_SHIFT_SKSNR)
#define BK1080_REG_06_MASK_SKCNT		(0x0FU << BK1080_REG_06_SHIFT_SKCNT)

#define BK1080_REG_06_GET_SKMUTER(x)		(((x) & BK1080_REG_06_MASK_SKMUTER) >> BK1080_REG_06_SHIFT_SKMUTER)
#define BK1080_REG_06_GET_SKMUTEA(x)		(((x) & BK1080_REG_06_MASK_SKMUTEA) >> BK1080_REG_06_SHIFT_SKMUTEA)
#define BK1080_REG_06_GET_SKSNR(x)		(((x) & BK1080_REG_06_MASK_SKSNR) >> BK1080_REG_06_SHIFT_SKSNR)
#define BK1080_REG_06_GET_SKCNT(x)		(((x) & BK1080_REG_06_MASK_SKCNT) >> BK1080_REG_06_SHIFT_SKCNT)

// REG 07

#define BK1080_REG_07_SHIFT_FREQD		4
#define BK1080_REG_07_SHIFT_SNR			0

#define BK1080_REG_07_MASK_FREQD		(0xFFFU << BK1080_REG_07_SHIFT_FREQD)
#define BK1080_REG_07_MASK_SNR			(0x00FU << BK1080_REG_07_SHIFT_SNR)

#define BK1080_REG_07_GET_FREQD(x)		(((x) & BK1080_REG_07_MASK_FREQD) >> BK1080_REG_07_SHIFT_FREQD)
#define BK1080_REG_07_GET_SNR(x)		(((x) & BK1080_REG_07_MASK_SNR) >> BK1080_REG_07_SHIFT_SNR)

// REG 10

#define BK1080_REG_10_SHIFT_STC			14
#define BK1080_REG_10_SHIFT_SFBL		13
#define BK1080_REG_10_SHIFT_AFCRL		12
#define BK1080_REG_10_SHIFT_STEN		9
#define BK1080_REG_10_SHIFT_ST			8
#define BK1080_REG_10_SHIFT_RSSI		0

#define BK1080_REG_10_MASK_STC			(0x01U << BK1080_REG_10_SHIFT_STC)
#define BK1080_REG_10_MASK_SFBL			(0x01U << BK1080_REG_10_SHIFT_SFBL)
#define BK1080_REG_10_MASK_AFCRL		(0x01U << BK1080_REG_10_SHIFT_AFCRL)
#define BK1080_REG_10_MASK_STEN			(0x01U << BK1080_REG_10_SHIFT_STEN)
#define BK1080_REG_10_MASK_ST			(0x01U << BK1080_REG_10_SHIFT_ST)
#define BK1080_REG_10_MASK_RSSI			(0xFFU << BK1080_REG_10_SHIFT_RSSI)

#define BK1080_REG_10_STC_NOT_COMPLETE		(0U << BK1080_REG_10_SHIFT_STC)
#define BK1080_REG_10_STC_COMPLETE		(1U << BK1080_REG_10_SHIFT_STC)

#define BK1080_REG_10_SFBL_SUCCESS		(0U << BK1080_REG_10_SHIFT_SFBL)
#define BK1080_REG_10_SFBL_FAILURE		(1U << BK1080_REG_10_SHIFT_SFBL)

#define BK1080_REG_10_AFCRL_NOT_RAILED		(0U << BK1080_REG_10_SHIFT_AFCRL)
#define BK1080_REG_10_AFCRL_RAILED		(1U << BK1080_REG_10_SHIFT_AFCRL)

#define BK1080_REG_10_STEN_MONO			(0U << BK1080_REG_10_SHIFT_STEN)
#define BK1080_REG_10_STEN_STEREO		(1U << BK1080_REG_10_SHIFT_STEN)

#define BK1080_REG_10_ST_MONO			(0U << BK1080_REG_10_SHIFT_ST)
#define BK1080_REG_10_ST_STEREO			(1U << BK1080_REG_10_SHIFT_ST)

#define BK1080_REG_10_GET_STC(x)		(((x) & BK1080_REG_10_MASK_STC) >> BK1080_REG_10_SHIFT_STC)
#define BK1080_REG_10_GET_SFBL(x)		(((x) & BK1080_REG_10_MASK_SFBL) >> BK1080_REG_10_SHIFT_SFBL)
#define BK1080_REG_10_GET_AFCRL(x)		(((x) & BK1080_REG_10_MASK_AFCRL) >> BK1080_REG_10_SHIFT_AFCRL)
#define BK1080_REG_10_GET_RSSI(x)		(((x) & BK1080_REG_10_MASK_RSSI) >> BK1080_REG_10_SHIFT_RSSI)

// REG 11

#define BK1080_REG_11_SHIFT_IMPC		10
#define BK1080_REG_11_SHIFT_READCHAN		0

#define BK1080_REG_11_MASK_IMPC			(0x00FU << BK1080_REG_11_SHIFT_IMPC)
#define BK1080_REG_11_MASK_READCHAN		(0x3FFU << BK1080_REG_11_SHIFT_READCHAN)

#define BK1080_REG_11_GET_IMPC(x)		(((x) & BK1080_REG_11_MASK_IMPC) >> BK1080_REG_11_SHIFT_IMPC)
#define BK1080_REG_11_GET_READCHAN(x)		(((x) & BK1080_REG_11_MASK_READCHAN) >> BK1080_REG_11_SHIFT_READCHAN)

#endif

